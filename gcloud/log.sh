#!/bin/bash

log(){
	local m="$*"
	echo -e "*** ${m} ***" >&3
	echo "=================================================================================" >&3
  local r="$*"
	echo "================================================================================="
	echo -e "*** $r ***" | sed -r "s/\x1B\[([0-9]{1,2}(;[0-9]{1,2})?)?[mGK]//g"
	echo "================================================================================="
}
